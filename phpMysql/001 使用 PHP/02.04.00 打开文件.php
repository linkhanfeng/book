<h3>4. 解决打开文件可能遇到的问题</h3>
<pre>
  1. 没有权限打开文件 failed to open stream: No such file or directory in ...
  赋权限: chmod 775 path/to/file
  2. 使用 错误抑制符 @ 给出友好的提示; (通常, 使用错误抑制操作并不是良好的代码风格, 只是简单的方法.)
</pre>
